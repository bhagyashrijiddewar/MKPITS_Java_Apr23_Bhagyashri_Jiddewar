

public class UnaryIncrementOperator{  
public static void main(String args[]){  
int x=10;  
System.out.println("Increment postfix : "+ x++);//10 (11)  
System.out.println("Increment prefix : "+ ++x);//12  
System.out.println("Decrement postfix : "+ x--);//12 (11)  
System.out.println("Decrement pretfix : "+ --x);//10  
}}  