public class SwitchStatment {
    public String switchStatment(String number){
        switch (number){
            case "1":
                    return  "Sunday";
            case "2":
                return  "Monday";
            case "3":
                return  "Tuesday";
            case "4":
                return  "Wednesday";
            case "5":
                return  "Thursday";
            case "6":
                return  "Friday";
            case "7":
                return  "Satarday";
        }
      return null;
    }
}
